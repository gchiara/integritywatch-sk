              <div class="panel-body">
                <p>Tento portál bol vyvinutý s cieľom riešiť problém prístupu k zásadným informáciám o financovaní slovenských politických strán, ktoré sa zvyčajne nachádzajú len v ich výročných správach. Tieto správy sú často prezentované na weboch politických strán vo formáte PDF, vďaka čomu nie sú pre užívateľov centrálne dostupné a prehľadné. Náš portál poskytuje inovatívne riešenie v podobe konsolidácie a komplexnej prezentácie dát zozbieraných za obdobie od roku 2002 do jesene roku 2023. K dispozícií sú interaktívne grafy a diagramy doplnené o nástroje, ako je fulltextové vyhľadávanie, či možnosť filtrovania informácií. Dáta v takejto podobe umožňujú používateľom jednoducho analyzovať trendy týkajúce sa financovania politických strán na Slovensku.</p>
                <p><strong>Politické dary:</strong> Táto podsekcia ponúka centralizovanú databázu politických darov deklarovaných vo výročných správach politických strán. Poskytuje používateľom flexibilitu pri selekcií konkrétnych kritérií vrátane výberu obdobia, konkrétnych strán, hlavných darcov a typov plnení. Interaktívne grafy je možné filtrovať a vizualizovať údaje na základe týchto kritérií. K dispozícii je aj graf zvýrazňujúci individuálne a kumulatívne dary presahujúce 10 000 eur, čím upozorňuje na zásadné príspevky od konkrétnej osoby pre určitú politickú stranu.</p>
                <p><strong>Politické pôžičky:</strong> Podobne môžu používatelia využiť rôzne kritériá na skúmanie informácií o pôžičkách, ktoré boli poskytnuté slovenským politickým stranám v období rokov 2002 - 2023, vrátane výberu konkrétneho obdobia, politických subjektov, poskytovateľov najvyšších pôžičiek, ale aj a typu pôžičky. Interaktívne grafy je možné prispôsobiť pomocou funkcie filtrovania a panel fulltextového vyhľadávania je možné použiť na nájdenie konkrétnych poskytovateľov pôžičiek alebo strán. Uvedený je aj ukazovateľ rizika, ktorý označuje individuálne aj kumulatívne úvery nad 100 000 eur.</p>
                <p><strong>Partneri verejného sektora:</strong> V tejto záložke portál poskytuje prepojenie medzi údajmi o daroch politických strán a Registrom partnerov verejného sektora na Slovensku. Táto integrácia má za cieľ odhaliť právnické osoby, ktoré poskytli dary politickým stranám a zároveň získali finančné prostriedky od štátu, čím sa zabezpečí transparentnosť týchto finančných transakcií. Používatelia môžu prechádzať interaktívnymi grafmi a identifikovať hlavné darcovské subjekty, ich právne štruktúry, počet konečných užívateľov výhod týchto entít, ako aj to, ktoré politické strany najčastejšie podporujú. Okrem toho sekcia ponúka výkonné možnosti vyhľadávania a filtre, ktoré používateľom poskytujú komplexný prehľad o vzťahu medzi politickým financovaním a zapojením verejného sektora.</p>
